public class Test063 {

	public static void main(String[] args) {

		int dan;		//-- 단 수의 변수
		int st;			//-- 공란과 별 입력을 위해 사용할 변수

		for (dan=1; dan<6; dan++)			//-- for 문으로 1 ~ 5단 범위 설정
		{
			for (st=5-dan; st>0; st--)		//-- 공란 입력 설정 → 총단수에서 입력된 단수를 뺀만큼 공란 입력
											//-- 1단일 경우 5 - 1 = 4 → 4만큼 공란 입력
											//-- st-- → 공란의 수가 1씩 감소
			{
				System.out.print(" ");
			}
			for (st=dan; st>0; st--)		//-- 별 입력 설정 → 총 단수에서 입력된 단수만큼 별 입력
											//-- 1단일 경우 st = dan → st = 1 이므로 별 1개 입력
											//-- st-- → dan은 5까지 설정되어있으나 st>0으로 설정되었기 때문에 st--로 설정
			{
				System.out.print("*");
			}
			for (st=dan-1; st>0; st--)		//-- 입력된 단 수에서 1을 뺀 만큼 별 입력(추가되는 별)
											//-- 1단일 경우 st = 1 - 1 = 0 이므로 별 0개 입력
											//-- 2단일 경우 st = 2 - 1 = 0 이므로 별 1개 입력
			{
				System.out.print("*");
			}
			System.out.println();			// 개행

		}

		for (dan=1; dan<5; dan++)			//-- for 문으로 6 ~ 9단 범위 설정 → dan < 5
		{
			for (st=dan; st>0; st--)		//-- 공란 입력 설정 → 총단수에서 입력된 단수 만큼 공란 입력
											//-- 2단일 경우 2 - 1 = 1 → 1칸만큼 공란 입력
											//-- st-- → dan은 5까지 설정되어있으나 st>0으로 설정되었기 때문에 st--로 설정
			{
				System.out.print(" ");
			}
			for (st=4-dan; st>0; st--)		//-- 별 입력 설정 → 6~9단 이므로 총 4개의 단이다. 4단에서 입력된 단수를 뺀만큼 별 입력
											//-- 1단일 경우 4 - 1 = 3 → 3만큼 별 입력
											//-- st-- → 별의 수가 1씩 감소
			{
				System.out.print("*");
			}
			for (st=dan; st<5; st++)		//-- 입력된 단 수만큼 별 입력(추가되는 별)
											//-- 1단일 경우 st = 1 이므로 별 1개 입력
											//-- 2단일 경우 st = 2 이므로 별 2개 입력
			{
				System.out.print("*");
			}
			System.out.println();			// 개행

		}
	}
}

